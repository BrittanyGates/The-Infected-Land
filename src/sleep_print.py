#!/usr/bin/python3
from time import sleep


def sleep_print() -> None:
    """
    This function introduces a four-second pause in between printing story lines on the terminal screen.
    :return: None
    """
    sleep(4)


def attack_sleep_print() -> None:
    """
    This function introduces a four-second pause in between printing battle result information on the terminal screen.
    :return: None
    """
    sleep(3)
